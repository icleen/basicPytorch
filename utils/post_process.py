def post_process_expected(pred_boxes, expected=1):
    for b, boxes in enumerate(pred_boxes):
        if boxes is not None:
            nboxes = torch.FloatTensor(expected, boxes.size(1)).fill_(0)
            for i in range(len(boxes)-1, -1, -1):
                obj_class = min(int(boxes[i, -1]), expected-1)
                nboxes[obj_class] = boxes[i]
            # nboxes = torch.maximum(nboxes, 0) # remove any less than 0's
            pred_boxes[b] = nboxes
    return pred_boxes


def regress_postp(preds):
    return torch.mean(preds.view(-1, 3, 4), dim=1)


def post_process_twoobj(pred_boxes):
    for b, boxes in enumerate(pred_boxes):
        if boxes is not None:
            nboxes = torch.FloatTensor(2, boxes.size(1)).fill_(0)
            for i in range(len(boxes)-1, -1, -1):
                nboxes[int(boxes[i, -1])] = boxes[i]
            pred_boxes[b] = nboxes
    return pred_boxes
